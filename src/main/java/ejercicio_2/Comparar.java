public class Comparar {

      public void compararNumeros(int a, int b){
        if(a == b){
            System.out.println("Son iguales");
        }else if(a > b){
            System.out.println("a es mayor que b");
        }else{
            System.out.println("b es mayor que a");
        }
      }
    }


